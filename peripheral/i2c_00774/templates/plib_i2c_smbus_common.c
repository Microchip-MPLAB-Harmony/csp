uint8_t SMBUSCRC8Byte(uint8_t initCRC, uint8_t data)
{
    return SMBUS_CRCTable[initCRC ^ data];
}

uint8_t SMBUSCRC8Buffer(uint8_t initCRC, void* pData, uint32_t size)
{
    uint8_t crc = initCRC;
    uint8_t* pDataBuffer = (uint8_t*)pData;

    while(size != 0U)
    {
        crc = crc ^ *pDataBuffer++;

        crc = SMBUS_CRCTable[crc];
        
        size--;
    }

    return(crc);
}